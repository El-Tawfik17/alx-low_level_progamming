#include "main.h"
/**
 * main - Entry point
 * Description:This program prints alphabet
 * in lowercase
 * Return: Always 0 (success)
 */
int main(void)
{
	void print_alphabet(void)
	{
		/*declaring variable*/
		char c;
		/*initialization*/
		c = 'a';
		while (c <= 'z')
		{
			_putchar('c');
			c++;
		}
		_putchar('\n');
	}
	return (0);
}
